import React from 'react';

interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'primary' | 'secondary' | 'danger' | 'ghost';
  size?: 'sm' | 'md' | 'lg';
  loading?: boolean;
}

const Button: React.FC<ButtonProps> = ({ 
  children, 
  variant = 'primary', 
  size = 'md', 
  loading, 
  className = '', 
  disabled,
  ...props 
}) => {
  
  const baseStyles = "rounded-xl font-bold transition-all duration-200 transform active:scale-95 focus:outline-none shadow-md flex items-center justify-center gap-2";
  
  const variants = {
    primary: "bg-gradient-to-r from-[#6B8EFF] to-[#5A7DDB] text-white hover:shadow-lg hover:brightness-110 border-2 border-transparent",
    secondary: "bg-white text-[#6B8EFF] border-2 border-[#6B8EFF] hover:bg-blue-50",
    danger: "bg-gradient-to-r from-red-400 to-red-500 text-white hover:shadow-lg",
    ghost: "bg-transparent text-gray-600 hover:bg-gray-100 shadow-none",
  };

  const sizes = {
    sm: "px-3 py-1.5 text-sm",
    md: "px-6 py-3 text-base",
    lg: "px-8 py-4 text-lg",
  };

  return (
    <button
      className={`${baseStyles} ${variants[variant]} ${sizes[size]} ${className} ${disabled || loading ? 'opacity-50 cursor-not-allowed transform-none' : ''}`}
      disabled={disabled || loading}
      {...props}
    >
      {loading ? (
        <svg className="animate-spin h-5 w-5 text-current" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
          <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
          <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
      ) : children}
    </button>
  );
};

export default Button;